#include<iostream>
using namespace std;

int main()
{
    int a = 123;

    cout << a << endl;

    char b = 'v';

    cout << b << endl;

    bool bl = true;

    cout << bl << endl;

    float f = 1.2;

    cout << f << endl;

    double d = 1.23;

    cout << d << endl;

    int a1 = 123456;
    char ch1 = a1;

    cout << ch1 << endl;

    unsigned int a2 = 112;

    cout << a2 << endl;

    unsigned int a3 = -112;

    cout << a3 << endl;

    cout << endl;

    int a4 = 2.0/5;
    float a5 = 2.0/5;
    double a6 = 2.0/5;

    cout << a4 << endl;
    cout << a5 << endl;
    cout << a6 << endl;

    cout << (2.0/5) << endl << endl;

    cout << "sizeof(a)  : " <<  sizeof(a)  << " bytes" << endl;
    cout << "sizeof(b)  : " <<  sizeof(b)  << " bytes" << endl;
    cout << "sizeof(bl) : " <<  sizeof(bl) << " bytes" << endl;
    cout << "sizeof(f)  : " <<  sizeof(f)  << " bytes" << endl;
    cout << "sizeof(d)  : " <<  sizeof(d)  << " bytes" << endl;

    return 0;
}









































